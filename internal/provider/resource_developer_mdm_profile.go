// setFullyKnown reports whether a set and all of its elements are known.
// ElementsAs cannot decode unknown elements into Go strings, so element-level
// validation must wait until apply, when references such as another resource's
// computed id have resolved.
func setFullyKnown(s types.Set) bool {
	if s.IsNull() || s.IsUnknown() {
		return false
	}
	for _, e := range s.Elements() {
		if e.IsUnknown() {
			return false
		}
	}
	return true
}

// validateDeveloperMDMProfile enforces local profile invariants mirrored from the backend.
func validateDeveloperMDMProfile(ctx context.Context, model developerMDMProfileModel) diag.Diagnostics {
	var diags diag.Diagnostics

	if setFullyKnown(model.PolicyIDs) {
		var policyIDs []string
		diags.Append(model.PolicyIDs.ElementsAs(ctx, &policyIDs, false)...)
		if len(policyIDs) == 0 {
			diags.AddAttributeError(
				path.Root("policy_ids"),
				"At least one policy is required",
				"`policy_ids` must contain at least one policy ID.",
			)
		}
		for _, id := range policyIDs {
			if id == "" {
				diags.AddAttributeError(
					path.Root("policy_ids"),
					"Empty policy ID",
					"`policy_ids` must not contain empty strings.",
				)
			}
		}
	}

	if model.Assignment.IsNull() || model.Assignment.IsUnknown() {
		return diags
	}

	var assignment developerMDMProfileAssignmentModel
	diags.Append(model.Assignment.As(ctx, &assignment, basetypes.ObjectAsOptions{})...)
	if diags.HasError() {
		return diags
	}

	var deviceIDs []string
	if setFullyKnown(assignment.DeviceIDs) {
		diags.Append(assignment.DeviceIDs.ElementsAs(ctx, &deviceIDs, false)...)
	}

	if assignment.AllDevices.ValueBool() && len(deviceIDs) > 0 {
		diags.AddAttributeError(
			path.Root("assignment"),
			"Conflicting assignment",
			"`all_devices = true` cannot be combined with `device_ids`. Use one or the other.",
		)
	}

	for _, id := range deviceIDs {
		if id == "" {
			diags.AddAttributeError(
				path.Root("assignment").AtName("device_ids"),
				"Empty device ID",
				"`device_ids` must not contain empty strings.",
			)
		}
	}

	return diags
}

// validateDeveloperMDMProfilePolicyCategories loads referenced policies and rejects
// more than one policy in the same category/target. The backend remains the source of truth.
func validateDeveloperMDMProfilePolicyCategories(ctx context.Context, client stepsecurityapi.Client, policyIDs []string) diag.Diagnostics {
	var diags diag.Diagnostics

	categoryTargetToPolicy := make(map[string]string)
	for _, id := range policyIDs {
		policy, err := client.GetDeveloperMDMPolicy(ctx, id)
		if err != nil {
			diags.AddAttributeError(
				path.Root("policy_ids"),
				"Could not load referenced policy",
				fmt.Sprintf("Failed to load policy %q referenced by this profile: %s", id, err.Error()),
			)
			continue
		}
		key := developerMDMCategoryTargetKey(policy.Category, policy.Target)
		if existing, ok := categoryTargetToPolicy[key]; ok {
			diags.AddAttributeError(
				path.Root("policy_ids"),
				"At most one policy per category/target",
				fmt.Sprintf(
					"Policies %q and %q both have category/target %q. A profile may reference at most one policy per category/target.",
					existing, id, key,
				),
			)
			continue
		}
		categoryTargetToPolicy[key] = id
	}

	return diags
}

func developerMDMCategoryTargetKey(category, target string) string {
	if target == "" {
		switch category {
		case stepsecurityapi.DeveloperMDMCategoryIDEExtension:
			target = stepsecurityapi.DeveloperMDMTargetVSCode
		case stepsecurityapi.DeveloperMDMCategoryPackageConfig:
			target = stepsecurityapi.DeveloperMDMTargetNPM
		}
	}
	return category + "#" + target
}

// buildDeveloperMDMProfileRequest converts the model into an API request body.
func buildDeveloperMDMProfileRequest(ctx context.Context, model developerMDMProfileModel, diags *diag.Diagnostics) stepsecurityapi.DeveloperMDMProfileRequest {
	req := stepsecurityapi.DeveloperMDMProfileRequest{
		Name:        model.Name.ValueString(),
		Description: model.Description.ValueString(),
		PolicyIDs:   sortedStringSet(ctx, model.PolicyIDs, diags),
	}

	if model.Assignment.IsNull() || model.Assignment.IsUnknown() {
		return req
	}

	var assignment developerMDMProfileAssignmentModel
	diags.Append(model.Assignment.As(ctx, &assignment, basetypes.ObjectAsOptions{})...)
	if diags.HasError() {
		return req
	}

	req.Assignment = stepsecurityapi.DeveloperMDMAssignment{
		AllDevices: assignment.AllDevices.ValueBool(),
		DeviceIDs:  sortedStringSet(ctx, assignment.DeviceIDs, diags),
	}

	return req
}

// applyDeveloperMDMProfileToModel applies an API profile response into the Terraform model.
// All backend policy IDs are preserved so future-category policies are not dropped.
func applyDeveloperMDMProfileToModel(ctx context.Context, profile *stepsecurityapi.DeveloperMDMProfile, model *developerMDMProfileModel, diags *diag.Diagnostics) {
	model.ID = types.StringValue(profile.ProfileID)
	model.ProfileID = types.StringValue(profile.ProfileID)
	model.Name = types.StringValue(profile.Name)
	model.CreatedBy = types.StringValue(profile.CreatedBy)
	model.CreatedAt = types.StringValue(profile.CreatedAt)
	model.UpdatedBy = types.StringValue(profile.UpdatedBy)
	model.UpdatedAt = types.StringValue(profile.UpdatedAt)

	if profile.Description != "" {
		model.Description = types.StringValue(profile.Description)
	} else {
		model.Description = types.StringNull()
	}

	model.PolicyIDs = stringSliceToSet(profile.PolicyIDs, diags)

	// An empty backend assignment (no all_devices, no device IDs) is cosmetically
	// equivalent to an omitted assignment block: both mean "unassigned". Collapse it
	// to null only when the prior config/state also omitted the block or already held
	// an explicit empty object. If the user wrote an explicit (empty) assignment
	// object, preserve that exact shape so a planned object value is not replaced by
	// null, which would fail Terraform's apply-consistency check. If the prior state
	// held a real assignment that the backend has since cleared, fall through to null
	// so the drift is surfaced on read.
	if !profile.Assignment.AllDevices && len(profile.Assignment.DeviceIDs) == 0 {
		if model.Assignment.IsNull() || model.Assignment.IsUnknown() {
			model.Assignment = types.ObjectNull(developerMDMAssignmentAttrTypes)
			return
		}

		var prior developerMDMProfileAssignmentModel
		diags.Append(model.Assignment.As(ctx, &prior, basetypes.ObjectAsOptions{})...)
		if diags.HasError() {
			return
		}
		if !prior.AllDevices.ValueBool() && len(prior.DeviceIDs.Elements()) == 0 {
			// Prior config/state was itself an explicit empty assignment; keep it verbatim.
			return
		}

		model.Assignment = types.ObjectNull(developerMDMAssignmentAttrTypes)
		return
	}

	deviceIDs := types.SetNull(types.StringType)
	if len(profile.Assignment.DeviceIDs) > 0 {
		deviceIDs = stringSliceToSet(profile.Assignment.DeviceIDs, diags)
	}

	assignmentObj, objDiags := types.ObjectValue(developerMDMAssignmentAttrTypes, map[string]attr.Value{
		"all_devices": types.BoolValue(profile.Assignment.AllDevices),
		"device_ids":  deviceIDs,
	})
	diags.Append(objDiags...)
	model.Assignment = assignmentObj
}

// stringSliceToSet converts a string slice into a Terraform set value.
func stringSliceToSet(values []string, diags *diag.Diagnostics) types.Set {
	if len(values) == 0 {
		return types.SetNull(types.StringType)
	}
	elems := make([]attr.Value, len(values))
	for i, v := range values {
		elems[i] = types.StringValue(v)
	}
	setValue, setDiags := types.SetValue(types.StringType, elems)
	diags.Append(setDiags...)
	return setValue
}
